class FurniturePurchaseForm(forms.Form):
    
    furniture = forms.ChoiceField(choices = (), widget=forms.Select( attrs={'id': 'id_furniture_1'}))
    quantity = forms.CharField(widget=forms.NumberInput( attrs={'id': 'id_quantity_1'}))

    def __init__(self, *args, **kwargs):
        super(FurniturePurchaseForm, self).__init__(*args, **kwargs)
        try:
            CHOICES = list([(str(furniture.id), str(furniture)) for furniture in FurnitureInventory.objects.all()])
            CHOICES.insert(0, (-1,'NONE'))
        except Exception as e:
            print('\n\n', e, '\n\n')
            CHOICES = [(-1, 'NONE')]
        self.fields['furniture'].choices = CHOICES

